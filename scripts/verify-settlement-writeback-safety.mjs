#!/usr/bin/env node
import fs from "fs";

function read(path) {
  return fs.readFileSync(path, "utf8");
}

function assertContains(label, haystack, needle) {
  if (!haystack.includes(needle)) {
    throw new Error(`${label}: missing required marker: ${needle}`);
  }
  console.log(`PASS: ${label}: found ${needle}`);
}

function assertNotContains(label, haystack, needle) {
  if (haystack.includes(needle)) {
    throw new Error(`${label}: forbidden marker present: ${needle}`);
  }
  console.log(`PASS: ${label}: does not contain ${needle}`);
}

const writebackRoute = read("app/api/settlements/writeback/route.ts");
const historyRoute = read("app/api/settlements/history/route.ts");
const writebackHelper = read("lib/settlementClioWriteback.ts");
const settlementPreview = read("lib/settlementPreview.ts");

console.log("=== VERIFY SETTLEMENT WRITEBACK ROUTE SAFETY ===");

assertContains("writeback route", writebackRoute, "const confirmWrite = body.confirmWrite === true");
assertContains("writeback route", writebackRoute, "Settlement writeback requires confirmWrite: true.");
assertContains("writeback route", writebackRoute, 'status: "blocked-missing-confirm-write"');
assertContains("writeback route", writebackRoute, 'status: "blocked-no-rows"');
assertContains("writeback route", writebackRoute, 'status: "blocked-readiness-validation"');
assertContains("writeback route", writebackRoute, 'status: "written-to-clio"');
assertContains("writeback route", writebackRoute, "noWritePerformed: false");
assertContains("writeback route", writebackRoute, "childBillMattersOnly: true");
assertContains("writeback route", writebackRoute, "noDocumentsGenerated: true");
assertContains("writeback route", writebackRoute, "noPrintQueueRecordsChanged: true");

const writeResultsIndex = writebackRoute.indexOf("const writeResults = []");
const writtenAuditIndex = writebackRoute.indexOf('status: "written-to-clio"');

if (writeResultsIndex < 0 || writtenAuditIndex < 0 || writtenAuditIndex < writeResultsIndex) {
  throw new Error("writeback route: written-to-clio audit must occur after writeResults are created.");
}
console.log("PASS: writeback route: written-to-clio audit occurs after writeResults path.");

console.log("");
console.log("=== VERIFY CLIO WRITEBACK HELPER SAFETY ===");

assertContains("writeback helper", writebackHelper, "If-Match");
assertContains("writeback helper", writebackHelper, "matter.etag");
assertContains("writeback helper", writebackHelper, "custom_field_values: payloadPlan.payload");
assertContains("writeback helper", writebackHelper, "upsertClaimIndexFromMatter(updatedMatter)");
assertContains("writeback helper", writebackHelper, "Settlement financial writeback is blocked for master matter");
assertContains("writeback helper", writebackHelper, "missingRequiredFields");
assertContains("writeback helper", writebackHelper, "PATCH");

console.log("");
console.log("=== VERIFY HISTORY ROUTE IS READ-ONLY ===");

assertContains("history route", historyRoute, "findMany");
assertContains("history route", historyRoute, "readOnly: true");
assertContains("history route", historyRoute, "localAuditHistoryOnly: true");
assertContains("history route", historyRoute, "noClioRecordsChanged: true");
assertContains("history route", historyRoute, "noDatabaseRecordsChanged: true");
assertContains("history route", historyRoute, "noDocumentsGenerated: true");
assertContains("history route", historyRoute, "noPrintQueueRecordsChanged: true");
assertNotContains("history route", historyRoute, ".create(");
assertNotContains("history route", historyRoute, ".update(");
assertNotContains("history route", historyRoute, ".delete(");
assertNotContains("history route", historyRoute, "clioFetch(");

console.log("");
console.log("=== VERIFY SETTLEMENT PREVIEW REMAINS NON-PERSISTENT ===");

assertContains("settlement preview", settlementPreview, "Preview only.");
assertContains("settlement preview", settlementPreview, "does not write to Clio");
assertContains("settlement preview", settlementPreview, "does not write to the database");
assertContains("settlement preview", settlementPreview, "does not generate documents");
assertContains("settlement preview", settlementPreview, "does not change the print queue");

console.log("");
console.log("=== SETTLEMENT WRITEBACK SAFETY VERIFICATION PASSED ===");
console.log("No Clio calls were made by this verifier.");
console.log("No database writes were made by this verifier.");
console.log("No documents were generated by this verifier.");
console.log("No print queue records were changed by this verifier.");
